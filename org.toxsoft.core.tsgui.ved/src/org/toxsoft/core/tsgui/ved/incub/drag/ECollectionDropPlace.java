package org.toxsoft.core.tsgui.ved.incub.drag;

/**
 * Drop place relative to collection item.
 *
 * @author vs
 */
public enum ECollectionDropPlace {
  /**
   * Dropped before the target
   */
  BEFORE,
  /**
   * Dropped after the target
   */
  AFTER,
  /**
   * Dropped on the target
   */
  ON,
  /**
   * Dropped into nothing
   */
  NOTHING
}
